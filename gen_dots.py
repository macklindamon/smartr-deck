def point_in_polygon(x, y, polygon):
    n = len(polygon)
    inside = False
    j = n - 1
    for i in range(n):
        xi, yi = polygon[i]
        xj, yj = polygon[j]
        if ((yi > y) != (yj > y)) and (x < (xj - xi) * (y - yi) / (yj - yi) + xi):
            inside = not inside
        j = i
    return inside

dots = []
y = STEP
while y < HEIGHT:
    x = STEP
    while x < WIDTH:
        for poly in continents:
            if point_in_polygon(x, y, poly):
                dots.append((x, y))
                break
        x += STEP
    y += STEP

# Output SVG circles
lines = []
per_line = 12
for i in range(0, len(dots), per_line):
    batch = dots[i:i+per_line]
    line = ''.join(f'<circle cx="{d[0]}" cy="{d[1]}" r="{RADIUS}" fill="{FILL}"/>' for d in batch)
    lines.append('      ' + line)

print(f'<!-- Dot-grid world map ({len(dots)} dots) -->')
